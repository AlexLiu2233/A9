    /**
     * Send a KV request with a specific message ID (for duplicate testing).
     */
    private static KVResponse sendRequestWithId(InetSocketAddress target, ByteString messageId,
                                                int command, ByteString key, ByteString value,
                                                int version) {
        // Build KVRequest
        KVRequest.Builder reqBuilder = KVRequest.newBuilder().setCommand(command);
        if (key != null) reqBuilder.setKey(key);
        if (value != null) reqBuilder.setValue(value);
        if (version != 0) reqBuilder.setVersion(version);

        ByteString payload = reqBuilder.build().toByteString();

        // Build Msg
        long checksum = computeChecksum(messageId, payload);
        byte[] msgBytes = Msg.newBuilder()
                .setMessageID(messageId)
                .setPayload(payload)
                .setCheckSum(checksum)
                .build()
                .toByteArray();

        // Send with retries
        for (int attempt = 0; attempt < RETRY_COUNT; attempt++) {
            try {
                DatagramPacket sendPacket = new DatagramPacket(
                        msgBytes, msgBytes.length, target.getAddress(), target.getPort());
                clientSocket.send(sendPacket);

                byte[] recvBuf = new byte[16 * 1024];
                DatagramPacket recvPacket = new DatagramPacket(recvBuf, recvBuf.length);
                clientSocket.receive(recvPacket);

                Msg respMsg = Msg.parseFrom(
                        ByteString.copyFrom(recvBuf, 0, recvPacket.getLength()));

                // Verify it's a response to our request
                if (!respMsg.getMessageID().equals(messageId)) {
                    // Got a response to a different request, try again
                    continue;
                }

                return KVResponse.parseFrom(respMsg.getPayload());
            } catch (SocketTimeoutException e) {
                // Retry
            } catch (Exception e) {
                // Parse error or other, retry
            }
        }
        return null; // All retries failed
    }

    private static long computeChecksum(ByteString messageId, ByteString payload) {
        CRC32 crc = new CRC32();
        crc.update(messageId.asReadOnlyByteBuffer());
        crc.update(payload.asReadOnlyByteBuffer());
        return crc.getValue();
    }

