    private static ByteString bs(String s) {
        return ByteString.copyFromUtf8(s);
    }

    private static void wipeAllNodes() {
        for (InetSocketAddress addr : nodeAddresses) {
            sendRequest(addr, CMD_WIPEOUT, null, null, 0);
        }
        sleep(200);
    }

    private static void sleep(long ms) {
        try { Thread.sleep(ms); } catch (InterruptedException ignored) {}
    }

    private static void parseArgs(String[] args) {
        if (args.length >= 1) {
            try { NUM_NODES = Integer.parseInt(args[0]); } catch (NumberFormatException ignored) {}
        }
        if (args.length >= 2) {
            try { BASE_PORT = Integer.parseInt(args[1]); } catch (NumberFormatException ignored) {}
        }
        if (args.length >= 3) {
            JAR_PATH = args[2];
        }

        System.out.println("Configuration:");
        System.out.println("  Nodes:     " + NUM_NODES);
        System.out.println("  Base port: " + BASE_PORT);
        System.out.println("  JAR path:  " + (JAR_PATH != null ? JAR_PATH : "(external — servers must be running)"));
        System.out.println();
    }

